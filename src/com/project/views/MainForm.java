    public void fillDataTable(List<NhanVien> listNV) {
        DefaultTableModel model = (DefaultTableModel) tblDanhSachNhanVien.getModel();
        model.setRowCount(0);
        for (NhanVien nv : listNV) {
            Object[] rowData = new Object[9];
            rowData[0] = nv.getMaNhanVien();
            rowData[1] = nv.getMatKhau();
            rowData[2] = nv.getTenNhanVien();
            rowData[3] = nv.getTuoi();
            rowData[4] = nv.isGioiTinh() ? "Nam" : "Nữ";
            rowData[5] = nv.isVaiTro() ? "Quản Lý" : "Nhân Viên";
            rowData[6] = nv.isTrangThai() ? "Đang Đi Làm " : "Đã Nghĩ";
            rowData[7] = nv.getEmail();
            rowData[8] = nv.getHinhAnh();
            model.addRow(rowData);
        }
    }

