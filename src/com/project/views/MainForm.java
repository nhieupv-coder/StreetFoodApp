    public ChamCong getFormChamCong(int i) throws ParseException {
        ChamCong chamCong = new ChamCong();
        chamCong.setMaNguoiChamCong(AuthenicatedHelper.nvDangNhap.getMaNhanVien());
        formater.applyPattern("yyyy-MM-dd");
        Date date = new Date();
        chamCong.setNgayDiLam(formater.parse(formater.format(date)));
        if (lblCaLam.getText().equals("Ca 1")) {
            chamCong.setCaLam(1);
        } else if (lblCaLam.getText().equals("Ca 2")) {
            chamCong.setCaLam(2);
        } else if (lblCaLam.getText().equals("Ca 3")) {
            chamCong.setCaLam(3);
        }
        String maNv = (String) tblNhanVienChamCong.getValueAt(i, 0);
        NhanVien nV = nvDAO.getNhanVienByID(maNv.trim());
        chamCong.setNhanVien(nV);
        boolean trangThai = (boolean) tblNhanVienChamCong.getValueAt(i, 4);
        chamCong.setTrangThai(trangThai);
        return chamCong;
    }

