    public void setFormNhanVien(NhanVien nv) {
        txtMaNhanVien.setText(nv.getMaNhanVien());
        txtMatKhau.setText(nv.getMatKhau());
        txtTenNhanVien.setText(nv.getTenNhanVien());
        txtTuoi.setText(String.valueOf(nv.getTuoi()));
        txtEmail.setText(nv.getEmail());
        if (nv.isGioiTinh()) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }
        if (nv.isVaiTro()) {
            rdoQuanLy.setSelected(true);
        } else {
            rdoNhanVien.setSelected(true);
        }
        if (nv.isTrangThai()) {
            rdoDangLamViec.setSelected(true);
        } else {
            rdoDaNghiViec.setSelected(true);
        }
        File file = new File("logos", nv.getHinhAnh());
        try {
            Image img = ImageIO.read(file);
            lblImage.setIcon(new ImageIcon(img.getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), 0)));
        } catch (IOException ex) {
            try {
                file = new File("logos", "No_image.png");
                Image img = ImageIO.read(file);
                lblImage.setIcon(new ImageIcon(img.getScaledInstance(lblImage.getWidth(), lblImage.getHeight(), 0)));
                lblImage.setToolTipText("No_image");
            } catch (IOException ex1) {
                Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex1);
            }
        }
    }

